public class Coffee {

    private int water;
    private int coffeeBeans;
    private int cost;

    public Coffee(int water, int coffeeBeans, int cost) {
        this.water = water;
        this.coffeeBeans = coffeeBeans;
        this.cost = cost;
    }

    public int getWater() {
        return water;
    }

    public int getCoffeeBeans() {
        return coffeeBeans;
    }

    public int getCost() {
        return cost;
    }

}
